/**
 * Checks if input looks like real English by matching against common word list
 * Returns true if at least one common English word is found
 */
export function looksEnglish(input) {
  const words = input.trim().toLowerCase().split(/\s+/)
  let englishMatches = 0

  for (let w of words) {
    w = w.replace(/[^a-z]/g, '')
    if (!w) continue
    if (ENGLISH_COMMON.has(w)) englishMatches++
  }

  return englishMatches > 0
}
